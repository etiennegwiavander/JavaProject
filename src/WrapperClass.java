public class WrapperClass {
    public static void main(String[] args){
        // wrapper class = provides a way to use primitive data types as reference data types.
        //                  reference data types contain useful methods
        //                  can be usef with collections (ex.ArrayList)

        // primitive        wapper
        // ________         ______
        // boolean          Boolean
        // char             Character
        // int              Integer
        // double           Double

        // autoboxing = the automatic conversion that the Java compiler makes between the primitive types and their corresponding wrapper class
        // unboxing = the reverse of autoboxing. Automatic conversion of wrapper class to primitve

        Boolean a = true;
        Character b = '@';
        Integer c = 1994;
        Double d = 3.34;

    }
}
